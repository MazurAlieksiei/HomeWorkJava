package home_work_1;

public class TaskMathExperiments {
    public static void main(String[] args) {
        System.out.println(Math.abs(-23)); // возвращает модуль числа
        System.out.println(Math.sqrt(16));//возвращает квадратный корень числа
        System.out.println(Math.cbrt(27)); //возвращает кубический корень числа
        System.out.println(Math.round(3.14)); //возвращает число, округленное до ближайшего целого числа
        System.out.println(Math.ceil(7.50)); //возвращает округленное число в большую сторону
        System.out.println(Math.floor(7.50)); //возвращает округленное число в меньшую сторону
        System.out.println(Math.max(7.50, 9.50)); //возвращает максимальное число из a и b
        System.out.println(Math.min(7.50, 9.50)); //возвращает минимальное число из a и b
        System.out.println(Math.pow(2, 3)); //возвращает число а, возведенное в степень b
        System.out.println(Math.random()); //возвращает случайное число от 0.0 до 1.0
        System.out.println(Math.rint(2.501)); //возвращает число, которое представляет ближайшее к заданному числу целое число
        System.out.println(Math.scalb(2, 3)); //возвращает произведение числа a на 2 в степени b
    }
}
